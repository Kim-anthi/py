#while loop

num = 5
while num <= 10:
    print(num)
    num += 1

num2 = 100
while num2 >= 0:
    print(num2)
    num2 -= 10

#for loop
jina = "Kim"
for i in jina:
    print(i)

cars = ["Audi", "Volvo", "Bmw"]
for Audi in cars:
    print(Audi)

streets = ["Moi", "Kenyatta", "Haille Sallasie"]
for Kenyatta in streets:
    print(Kenyatta)

nu = 1000
while nu >= 0:
    print(nu)
    nu -= 100
